import math

# 1) Количество кратчайших путей в сетке 16x16 без ограничений
# Кратчайший путь состоит из 16 шагов вправо (R) и 16 шагов вверх (U)
# Общее количество шагов: 16 + 16 = 32
# Количество путей = C(32, 16) = 32! / (16! * 16!)
result_1 = math.comb(32, 16)

# 2) Количество кратчайших путей без двух подряд идущих шагов вверх (U)
# Сначала расставляем все 16 шагов вправо (R), создавая 17 возможных промежутков
# (перед первым R, между R и после последнего R)
# Нужно разместить 16 шагов U в эти 17 промежутков, не более одного U в каждый промежуток
# Количество способов = C(17, 16) = 17
result_2 = math.comb(17, 16)

print("Количество путей в сетке 16×16 без ограничений:", result_1)
print("Количество путей без двух подряд идущих U:", result_2)